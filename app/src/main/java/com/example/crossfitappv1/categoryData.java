package com.example.crossfitappv1;

public class categoryData {
    private String name,description,availableDate,expireDate,image,key;

    public categoryData() {
    }

    public categoryData(String name, String description, String availableDate, String expireDate, String image, String key) {
        this.name = name;
        this.description = description;
        this.availableDate = availableDate;
        this.expireDate = expireDate;
        this.image = image;
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getAvailableDate() {
        return availableDate;
    }

    public void setAvailableDate(String availableDate) {
        this.availableDate = availableDate;
    }

    public String getExpireDate() {
        return expireDate;
    }

    public void setExpireDate(String expireDate) {
        this.expireDate = expireDate;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
